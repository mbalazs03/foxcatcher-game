package foxcatcher.model;

/**
 * This enum class represents the fields of the board.
 */
public enum Field {
    /**
     * Represents the Hound piece on the board.
     */
    DARK,
    /**
     * Represents the Fox piece on the board.
     */
    LIGHT,
    /**
     * Represents the empty field on the board.
     */
    EMPTY

}
